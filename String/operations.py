#concatination

a = "Hello"
b = "World"
c = a + b
print(c)

#repetition
d = a * 3
print(d)

#length 
e = len(a)
print(e)

#indexing 
f = a[1]
print(f)
#in indexing we can not assign the value to the index

#slicing
g = a[1:4]
print(g)
#in slicing last index is not included
#in slicing negative indexing is also possible
h = a[-3:-1]
print(h)
